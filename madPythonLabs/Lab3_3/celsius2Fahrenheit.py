# Celsius to Fahrenheit conversion
import math
math.degrees()

celsius = input("Enter temp in celsius: ")

fahrenheit = float(float(celsius) * (9 / 5)) + 32

print("Your Temperature is: " + str(fahrenheit) + '\u1D3C' "F")

